const dictionaries: Record<Locale, RawDictionary> = {
  "en": en,
  "zh-CN": zhCN,
  "ja": ja,
}

const flatDictionaries = {
  "en": flatten(dictionaries.en),
  "zh-CN": flatten(dictionaries["zh-CN"]),
  "ja": flatten(dictionaries.ja),
}

const LOCALE_STORAGE_KEY = "foursmith-vr-locale"

export const LANGUAGE_OPTIONS = [
  { value: "zh-CN", label: "简体中文" },
  { value: "en", label: "English" },
  { value: "ja", label: "日本語" },
] as const satisfies readonly { value: Locale, label: string }[]

const getInitialLocale = (): Locale => {
  if (typeof window === "undefined") return "en"
  const stored = window.localStorage.getItem(LOCALE_STORAGE_KEY)
  if (LANGUAGE_OPTIONS.some(option => option.value === stored)) return stored as Locale
  return "en"
}

const [locale, setLocaleSignal] = createSignal<Locale>(getInitialLocale())

const syncDocumentLocale = (nextLocale: Locale) => {
  if (typeof document === "undefined") return
  document.documentElement.lang = nextLocale
}

syncDocumentLocale(locale())

export { locale }

export const setLocale = (nextLocale: Locale) => {
  setLocaleSignal(nextLocale)
  if (typeof window !== "undefined") window.localStorage.setItem(LOCALE_STORAGE_KEY, nextLocale)
  syncDocumentLocale(nextLocale)
}

